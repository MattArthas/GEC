package cc.zero.travel.model;


public class Dishes {

	private Integer dishesId;

    private String dishesName;

    private Integer dishesPrice;

    private String dishesCategory;

    private Integer dishesSalesVolume;

    private String dishesIntroduce;

    private String dishesComment;
    
    private String picName;

	public Integer getDishesId() {
		return dishesId;
	}

	public void setDishesId(Integer dishesId) {
		this.dishesId = dishesId;
	}

	public String getDishesName() {
		return dishesName;
	}

	public void setDishesName(String dishesName) {
		this.dishesName = dishesName;
	}

	public Integer getDishesPrice() {
		return dishesPrice;
	}

	public void setDishesPrice(Integer dishesPrice) {
		this.dishesPrice = dishesPrice;
	}

	public String getDishesCategory() {
		return dishesCategory;
	}

	public void setDishesCategory(String dishesCategory) {
		this.dishesCategory = dishesCategory;
	}

	public Integer getDishesSalesVolume() {
		return dishesSalesVolume;
	}

	public void setDishesSalesVolume(Integer dishesSalesVolume) {
		this.dishesSalesVolume = dishesSalesVolume;
	}

	public String getDishesIntroduce() {
		return dishesIntroduce;
	}

	public void setDishesIntroduce(String dishesIntroduce) {
		this.dishesIntroduce = dishesIntroduce;
	}

	public String getDishesComment() {
		return dishesComment;
	}

	public void setDishesComment(String dishesComment) {
		this.dishesComment = dishesComment;
	}

	public String getPicName() {
		return picName;
	}

	public void setPicName(String picName) {
		this.picName = picName;
	}

	
}
