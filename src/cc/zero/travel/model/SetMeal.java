package cc.zero.travel.model;


public class SetMeal {
	private Integer setMealId;

    private String setMealName;

    private Integer setMealPrice;

    private Integer setMealSaleVolume;

    private String setMealIntroduce;

    private String setMealComment;

	public Integer getSetMealId() {
		return setMealId;
	}

	public void setSetMealId(Integer setMealId) {
		this.setMealId = setMealId;
	}

	public String getSetMealName() {
		return setMealName;
	}

	public void setSetMealName(String setMealName) {
		this.setMealName = setMealName;
	}

	public Integer getSetMealPrice() {
		return setMealPrice;
	}

	public void setSetMealPrice(Integer setMealPrice) {
		this.setMealPrice = setMealPrice;
	}

	public Integer getSetMealSaleVolume() {
		return setMealSaleVolume;
	}

	public void setSetMealSaleVolume(Integer setMealSaleVolume) {
		this.setMealSaleVolume = setMealSaleVolume;
	}

	public String getSetMealIntroduce() {
		return setMealIntroduce;
	}

	public void setSetMealIntroduce(String setMealIntroduce) {
		this.setMealIntroduce = setMealIntroduce;
	}

	public String getSetMealComment() {
		return setMealComment;
	}

	public void setSetMealComment(String setMealComment) {
		this.setMealComment = setMealComment;
	}

	
	
}
